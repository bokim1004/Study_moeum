<div>공간 복잡도</div>;
<div>
  기본적으로 시간복잡도와 동일하다. 다만 처리 시간대신 메모리 사용량의 변화를
  비교하는 것만 다를 뿐. 총 필요 저장공간은 고정공간(알고리즘과
  무관),가변공간으로 나눠진다. 고정공간 : 코드 저장공간, 단순 변수 및 상수
  가변공간 : 알고리즘 실행과 관련있으며 실행 중, 동적으로 필요한 공간
</div>;

